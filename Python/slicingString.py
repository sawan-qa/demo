#String Slicing - We can return the range of character
name = "Atul Kumar Singh"
print(name[2:5])
#slice from the start
print(name[:5])
#Slice from the end
print(name[2:])
#Slice from inverse
print(name[-5:-2])
#reverse a string
print(name[::-1])
#slice with other method
name1=name[2:5]
name1=name1[::-1]
print(name1.upper())